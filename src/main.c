static void canvas_update_proc(Layer *layer, GContext *ctx) {
  // Background cream color
  graphics_context_set_fill_color(ctx, GColorFromRGB(240, 237, 232));
  graphics_fill_rect(ctx, layer_get_bounds(layer), 0, GCornerNone);

  // Draw illustration pixels
  draw_background(ctx);

  // ── LIVE TIME over screen area ──
  // Screen is tilted ~7deg; position time in top-right of screen
  graphics_context_set_text_color(ctx, GColorBlack);
  graphics_draw_text(ctx,
    s_time_buffer,
    fonts_get_system_font(FONT_KEY_LECO_32_BOLD_NUMBERS),
    GRect(37, 28, 64, 40),
    GTextOverflowModeWordWrap,
    GTextAlignmentRight,
    NULL);

  // ── LIVE BATTERY FILL over battery icon area ──
  // Battery outline is at approx x=61..99, y=110..128 (tilted)
  // Fill proportionally
  int fill_w = (s_battery_percent * 32) / 100;
  graphics_context_set_fill_color(ctx, GColorBlack);
  if (fill_w > 0) {
    graphics_fill_rect(ctx, GRect(62, 111, fill_w, 15), 0, GCornerNone);
  }

  // ── BATTERY % TEXT ──
  static char batt_buf[8];
  snprintf(batt_buf, sizeof(batt_buf), "%d%%", s_battery_percent);
  graphics_context_set_text_color(ctx, GColorBlack);
  graphics_draw_text(ctx,
    batt_buf,
    fonts_get_system_font(FONT_KEY_GOTHIC_14),
    GRect(37, 68, 64, 18),
    GTextOverflowModeWordWrap,
    GTextAlignmentRight,
    NULL);
}

static void main_window_load(Window *window) {
  Layer *window_layer = window_get_root_layer(window);
  s_canvas_layer = layer_create(layer_get_bounds(window_layer));
  layer_set_update_proc(s_canvas_layer, canvas_update_proc);
  layer_add_child(window_layer, s_canvas_layer);
  update_time();
}

static void main_window_unload(Window *window) {
  layer_destroy(s_canvas_layer);
}

static void init() {
  s_main_window = window_create();
  window_set_window_handlers(s_main_window, (WindowHandlers) {
    .load = main_window_load,
    .unload = main_window_unload,
  });
  window_stack_push(s_main_window, true);
  tick_timer_service_subscribe(MINUTE_UNIT, (TickHandler)update_time);
  battery_state_service_subscribe(battery_callback);
  battery_callback(battery_state_service_peek());
}

static void deinit() {
  tick_timer_service_unsubscribe();
  battery_state_service_unsubscribe();
  window_destroy(s_main_window);
}

int main(void) {
  init();
  app_event_loop();
  deinit();
}
